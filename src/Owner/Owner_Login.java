    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Email="",Password="";
        Email = jTextField1.getText();
        if(Email.isEmpty()== true)
        {
            JOptionPane.showMessageDialog(rootPane,"Enter Email");
            return;
        }
        
        Password = jTextField2.getText();
        if(Password.isEmpty()== true)
        {
            JOptionPane.showMessageDialog(rootPane,"Enter Password");
            return;
        }
        
        
        //Database Connection Establishment
         try
        {  
            conn=DriverManager.getConnection(URL,User,Passwords);
            stmt=conn.createStatement();
            rs=stmt.executeQuery("Select Count(Email) From Owner Where Email = '"+Email+"' and Password='"+Password+"';");
              while(rs.next())
            {
                Check = Integer.parseInt(rs.getString(1));
            }
 
              if(Check==0)
              {
                  jLabel4.setText("Invalid Details");
                  jLabel4.setForeground(Color.RED);
              }
              else
              {
                  jLabel4.setText("Login Successful");
                  jLabel4.setForeground(Color.BLUE);
                  Owner_Dashboard OD = new Owner_Dashboard();
                  OD.setVisible(true);
                  new Owner_Dashboard(Email);
                  dispose();
               }
               
           rs.close();
            stmt.close();
            conn.close();
       
            }
       
                
       catch(SQLException e)
        {
            System.out.print("  SQL EXCEPTION  "+e.getMessage());
        }
        catch(Exception ex)
        {
           System.out.print("  EXCEPTION  "+ex.getMessage());
        }  
        
    }//GEN-LAST:event_jButton1ActionPerformed

