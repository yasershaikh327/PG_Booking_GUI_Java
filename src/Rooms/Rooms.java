     public void GetDetails()
    {
        //Code
          int rows=0,cols=0;
                //int i = 0;
                 //DefaultTableModel model=( DefaultTableModel) x.getModel();
        try
        {  
            conn=DriverManager.getConnection(URL,User,Passwords);
            stmt=conn.createStatement();
            rs=stmt.executeQuery("Select P.Name,R.Room_Type,R.Price,P.Location From PG P,Room R,Owner O WHERE (O.Owner_ID=P.Owner_ID) AND (P.PGID=R.PGID);");
             
            while(rs.next())
            {
               Room_Display.setValueAt(rs.getString(1), rows, cols++);
               Room_Display.setValueAt(rs.getString(2), rows, cols++);
               Room_Display.setValueAt(rs.getString(3), rows, cols++);
               Room_Display.setValueAt(rs.getString(4), rows, cols++);
               Room_Display.addNotify();
               rows++;
               cols=0;
             
   
            }
 
            rs.close();
            stmt.close();
            conn.close();
       
            }
       
                
       catch(SQLException e)
        {
            System.out.print("  SQL EXCEPTION  "+e.getMessage());
        }
        catch(Exception ex)
        {
           System.out.print("  EXCEPTION  "+ex.getMessage());
        }  
    }
    
    
